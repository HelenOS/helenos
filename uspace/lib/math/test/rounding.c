static inline uint32_t fint(float x)
{
	union {
		float f;
		uint32_t i;
	} u = { .f = x };
	return u.i;
}

static inline uint64_t dint(double x)
{
	union {
		double f;
		uint64_t i;
	} u = { .f = x };
	return u.i;
}

#define assert_float_equals(x, y) PCUT_ASSERT_EQUALS(fint(x), fint(y))
#define assert_double_equals(x, y) PCUT_ASSERT_EQUALS(dint(x), dint(y))

